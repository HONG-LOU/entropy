export function detectLanguage(storage = globalThis.localStorage, languages = globalThis.navigator?.languages || []) {
  const saved = storage?.getItem(STORAGE_KEY);
  if (saved === "zh" || saved === "en") return saved;
  return languages.some((language) => language.toLowerCase().startsWith("zh")) ? "zh" : "en";
}

export function translate(language, key, values = {}) {
  const template = messages[language]?.[key] ?? messages.zh[key] ?? key;
  return Object.entries(values).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, String(value)), template);
}

export function saveLanguage(language, storage = globalThis.localStorage) {
  storage?.setItem(STORAGE_KEY, language);
}
